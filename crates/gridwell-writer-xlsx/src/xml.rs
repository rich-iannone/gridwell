/// Convert column index (0-based) to Excel column letter(s).
/// 0 -> A, 1 -> B, ..., 25 -> Z, 26 -> AA, etc.
pub fn col_letter(col: usize) -> String {
    let mut result = String::new();
    let mut n = col;
    loop {
        result.insert(0, (b'A' + (n % 26) as u8) as char);
        if n < 26 {
            break;
        }
        n = n / 26 - 1;
    }
    result
}

/// Build a cell reference like "A1", "B2", etc. (1-based row).
pub fn cell_ref(col: usize, row: usize) -> String {
    format!("{}{}", col_letter(col), row)
}
